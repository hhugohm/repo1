    private void btnConfigMessageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfigMessageActionPerformed
        
        Mensaje mensaje = new Mensaje();
        mensaje.setTituloMensaje(this.txtTituloMensaje.getText());
        mensaje.setRequestMensaje("");
        mensaje.setResponseMensaje("");
        
        Conexion conexion = seleccion.getConexion();
        conexion.addMessage(mensaje);
        mensaje.setConexion(conexion);
        
        NodeMQUI<String> nodo =
                    new NodeMQUI<String>(mensaje.getTituloMensaje(),
                    false,
                    createImageIcon(
                    ConstantsBroker.IMAGE_NEWMESAAGE));
            nodo.setMensaje(mensaje);
            seleccion.add(nodo);  
            
            modelo.insertNodeInto(nodo, seleccion, seleccion.getChildCount()-1);
            
            
           MensajeDao mensajedao = (MensajeDao)  
                        getInstance().getContext().
                                    getBean("mensajeDao");
        
           mensajedao.saveOrUpdate(mensaje);
           
           
           MQTabbedMessage tabs=mqdesk.getTabMessages();
                //no hay pesta√±as                
           mqdesk.getJpanelRight().add(tabs);
                     
            tabs.addTab(conexion.getIdConexion() +  "," +
                    mensaje.getTituloMensaje(),GuiUtils.createImageIcon("/icons/plugin.gif")
						,new PanelSplitMessage(nodo));  
           
            
            this.setVisible(false);
            
            
            
    }//GEN-LAST:event_btnConfigMessageActionPerformed

